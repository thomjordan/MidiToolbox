// ---------------------------------------------------------------------------------------------------------------------
// MIDIUserEventDataEx, USER DATA in MusicSequence
// ---------------------------------------------------------------------------------------------------------------------

public struct MusicEventUserDataEx
{
    public var length: UInt32
    public var data: MIDIRawDataExtension
    
    public init()
    {
        length = 0
        data = MIDIRawDataExtension()
    }
}


// ---------------------------------------------------------------------------------------------------------------------
// MyMusicEventUserData
// ---------------------------------------------------------------------------------------------------------------------

class MyMusicEventUserData
{
    private var ext: MusicEventUserDataEx = MusicEventUserDataEx()
    
    public let MusicEventUserDataPtr : UnsafeMutablePointer<MusicEventUserData>
    public let dataPtr : UnsafeMutablePointer<(UInt8)> // data access
    
    init(data: [UInt8])
    {
        MusicEventUserDataPtr = withUnsafeMutablePointer(to: &ext)
        {
            $0.withMemoryRebound(to: MusicEventUserData.self, capacity: 1)
            {
                pointer in
                return pointer
            }
        }
        
        var len = data.count
        if len > MemoryLayout<MIDIRawDataExtension>.size
        {
            len = MemoryLayout<MIDIRawDataExtension>.size
        }
        
        dataPtr = withUnsafeMutablePointer(to: &ext.data.bytes)
        {
            $0.withMemoryRebound(to: (UInt8).self, capacity: len)
            {
                pointer in
                return pointer
            }
        }
        
        withUnsafePointer(to: &ext.data.bytes.0)
        {
            p in
            let p = UnsafeMutablePointer<UInt8>(mutating: p)
            for i in 0..<len
            {
                p[i] = data[i]
            }
        }
        
        ext.length = UInt32(len)
    }
}

